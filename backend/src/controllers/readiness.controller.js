export const submitDailyReadiness = async (req, res) => {
  try {
    const {
      userId,
      date,
      sleepHours,
      stressLevel,
      subjectiveFeeling,
      restingHeartRate,
      hydrationLevelPercent,
      sorenessAreas
    } = req.body;

    if (!userId || !date || sleepHours == null || !stressLevel || !subjectiveFeeling) {
      return res.status(400).json({
        message: "Missing required readiness inputs"
      });
    }

    /* STRICT UTC NORMALIZATION */
    const [year, month, day] = date.split("-").map(Number);
    const normalizedDate = new Date(Date.UTC(year, month - 1, day));

    const { readinessScore, readinessCategory } = calculateReadiness({
      sleepHours,
      stressLevel,
      subjectiveFeeling,
      restingHeartRate,
      hydrationLevelPercent
    });

    const readiness = await DailyReadiness.findOneAndUpdate(
      { userId, date: normalizedDate },
      {
        userId,
        date: normalizedDate,
        sleepHours,
        stressLevel,
        subjectiveFeeling,
        restingHeartRate,
        hydrationLevelPercent,
        sorenessAreas,
        readinessScore,
        readinessCategory,
        autoGenerated: false
      },
      { upsert: true, new: true }
    );

    res.status(200).json({
      message: "Daily readiness recorded",
      readiness
    });

  } catch (error) {
    res.status(500).json({
      message: "Failed to record readiness",
      error: error.message
    });
  }
};


// Get readiness history
export const getDailyReadiness = async (req, res) => {
  try {
    const { userId } = req.params;

    const readinessHistory = await DailyReadiness.find({ userId })
      .sort({ date: -1 })
      .limit(30);

    res.json(readinessHistory);
  } catch (error) {
    res.status(500).json({
      message: "Failed to fetch readiness history"
    });
  }
};
